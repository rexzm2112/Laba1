#include <stdio.h>

int main() {
    int d = 654;
    int a = d % 10; // остання цифра числа d
    int bc = (d - a) / 10; // залишок після видалення останньої цифри
    int b = bc % 10; // цифра десятків залишку
    int c = bc / 10; // цифра сотень залишку
    int x = a * 100 + b * 10 + c; // відновлення числа x
    printf("x = %d\n", x); // виведення результату
    return 0;
}
